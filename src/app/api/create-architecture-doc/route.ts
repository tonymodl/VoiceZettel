import { NextResponse } from "next/server";
import { driveCreateDoc, docsInsertText } from "@/lib/googleClient";
import { logger } from "@/lib/logger";

/**
 * POST /api/create-architecture-doc
 * Creates a Google Doc with the complete VoiceZettel architecture
 * explanation for NotebookLM podcast generation.
 */
export async function POST() {
    try {
        const title = "VoiceZettel — Полная архитектура голосового AI-ассистента (для NotebookLM подкаста)";
        
        const doc = await driveCreateDoc(title);
        const content = buildArchitectureContent();
        
        await docsInsertText(doc.id, content, 1);
        
        logger.info(`[ArchitectureDoc] Created: ${doc.url}`);
        
        return NextResponse.json({
            success: true,
            documentId: doc.id,
            url: doc.url,
            title,
        });
    } catch (err) {
        logger.error(`[ArchitectureDoc] Failed: ${err}`);
        return NextResponse.json(
            { error: err instanceof Error ? err.message : "Unknown error" },
            { status: 500 },
        );
    }
}

