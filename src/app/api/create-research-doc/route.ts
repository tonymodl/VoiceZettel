import { NextResponse } from "next/server";
import { driveCreateDoc, docsInsertText } from "@/lib/googleClient";
import { logger } from "@/lib/logger";

/**
 * POST /api/create-research-doc
 * Creates a Google Doc with context window optimization research.
 */
export async function POST() {
    try {
        const title = "VoiceZettel — Исследование: Оптимизация контекстного окна и UX голосового ассистента";
        
        // Create document
        const doc = await driveCreateDoc(title);
        
        // Build comprehensive research content
        const content = buildResearchContent();
        
        // Google Docs API: insertText at index 1 (beginning of document)
        // We need to insert in reverse order since each insert pushes content forward
        await docsInsertText(doc.id, content, 1);
        
        logger.info(`[ResearchDoc] Created: ${doc.url}`);
        
        return NextResponse.json({
            success: true,
            documentId: doc.id,
            url: doc.url,
            title,
        });
    } catch (err) {
        logger.error(`[ResearchDoc] Failed: ${err}`);
        return NextResponse.json(
            { error: err instanceof Error ? err.message : "Unknown error" },
            { status: 500 },
        );
    }
}

